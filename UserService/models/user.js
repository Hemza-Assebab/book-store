const mongoose = require("mongoose");
require("dotenv").config();

const UserSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
    role: {
        type: String,
        required: true,
        enum: ["admin", "user"],
    }
});

const User = mongoose.model("User", UserSchema);

module.exports = User;